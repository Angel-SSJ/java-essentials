/**
 * The interface Bird interface.
 */
public interface BirdInterface extends Aerial {
    /**
     * Fly.
     */
    void fly();

    /**
     * Glide.
     */
    void glide();

    /**
     * Take off.
     */
    void takeOff();

    /**
     * Land.
     */
    void land();

    /**
     * Lay eggs.
     */
    void layEggs();

    /**
     * Peck.
     */
    void peck();
}