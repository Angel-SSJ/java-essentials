/**
 * The interface Aerial.
 */
public interface Aerial extends CommonActions {
    /**
     * Fly.
     */
    void fly();

    /**
     * Glide.
     */
    void glide();

    /**
     * Take off.
     */
    void takeOff();

    /**
     * Land.
     */
    void land();

    /**
     * Make sound.
     */
    void makeSound();

    /**
     * Eat.
     */
    void eat();

    /**
     * Sleep.
     */
    void sleep();
}