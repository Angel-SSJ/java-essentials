/**
 * The interface Aquatic.
 */
public interface Aquatic extends CommonActions {
    /**
     * Swim.
     */
    void swim();

    /**
     * Dive.
     */
    void dive();

    /**
     * Emerge.
     */
    void emerge();

    /**
     * Make sound.
     */
    void makeSound();

    /**
     * Eat.
     */
    void eat();

    /**
     * Sleep.
     */
    void sleep();
}
